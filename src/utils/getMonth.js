export const getMonthFunction = (month) => {
  switch (month) {
    case 1:
      return "январь";
    case 2:
      return "февраль";
    case 3:
      return "март";
    case 4:
      return "aпрель";
    case 5:
      return "май";
    case 6:
      return "июнь";
    case 7:
      return "июль";
    case 8:
      return "aвгуст";
    case 9:
      return "cентябрь";
    case 10:
      return "oктябрь";
    case 11:
      return "ноябрь";
    case 12:
      return "декабрь";

    default:
      break;
  }
};
